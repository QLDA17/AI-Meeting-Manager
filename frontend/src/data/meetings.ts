    mockMeetings.filter(m => m.groupId === groupId);
export const getMeetingsByOrgId = (orgId: string): Meeting[] =>
    mockMeetings.filter(m => m.orgId === orgId);
export const getMeetingsByUserId = (userId: string): Meeting[] =>
    mockMeetings.filter(m => m.attendees.some(a => a.id === userId) || m.createdBy === userId);
